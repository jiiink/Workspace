from sympy import *

"""
`3x```-6y````+``z`````+`5u```-`4v````=````````2``#
`x````+`4y```-2z```+````u`````+`8v````=````````1`#
`x```+`2y```+`3z```+`4u`````-``v``````=``````11`#
-x````-`y````+``5z```-`3u``````+`v`````=``-1``#
`2x```-`5y```````-`z``````+3u```-`3v`````=`-2`
"""

x, y, z, u, v = symbols('x, y, z,u,v')


Ma=Matrix([[3,-6,1,5,-4],[1,4,-2,1,8], [1,2,3,4,-1],\
            [-1,-1,5,-3,1], [2,-5,-1,3,-3]])
Mb=Matrix([2, 1, 11, -1,-2])
sol = linsolve((Ma,Mb), (x,y,z,u,v))
print( sol )