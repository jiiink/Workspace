

# 친구가 N명, 전화번호
pn = ['010-4323-1899', '010-1234-6745', '4567', 'NONE', \
      '011-9090-8767', '01-7656-1123']
fn = ['팽길탄', "여척두", "쉰천지", "이명복", \
      "민병훈", "마차돌" ]

fname = input("친구이름을 치시요:" )
print( f' 친구이름: { fname }')

idx = fn.index(fname)

print( f' 친구{ fname }의 번호는 { pn[idx]}')