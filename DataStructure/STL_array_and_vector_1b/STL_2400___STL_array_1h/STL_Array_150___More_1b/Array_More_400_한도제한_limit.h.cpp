#include <bits/stdc++.h>
#include <limits.h>

using namespace std;

const int cmin_int = INT_MIN;
const int cmax_int = INT_MAX;
const int cppmin_int = numeric_limits<int>::min();
const int cppmax_int = numeric_limits<int>::max();

int main() {



    cout << "\n 정수 최소값, cmin_int : " << cmin_int  ;
    cout << "\n 정수 최대값, cmax_int : " << cmax_int ;
    cout << "\n C++에서 정수 최소값, cppmin_int : " << cppmin_int ;
    cout << "\n C++에서 정수 최대값, cppmax_int : " << cppmax_int  ;

return 0;
}


