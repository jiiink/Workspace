def print_grid_with_path(N, path):
    grid_with_path = [['-' for _ in range(N)] for _ in range(N)]

    for point in path:
        x, y, _ = point
        grid_with_path[y - 1][x - 1] = '*'

    for row in grid_with_path:
        print(' '.join(row))

